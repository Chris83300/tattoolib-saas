    <li>Contrôle d'accès basé sur les rôles (RBAC)</li>
    <li>Pare-feu applicatif et middleware de sécurité (CSP, rate limiting, protection CSRF)</li>
    <li>Analyse antivirus des fichiers uploadés</li>
    <li>Journalisation des accès et monitoring de sécurité</li>
    <li>Sauvegardes régulières et chiffrées</li>
    <li>Données bancaires jamais stockées par Ink&amp;Pik (traitées exclusivement par Stripe, certifié PCI DSS)</li>
</ul>

<p>En cas de violation de données, Ink&amp;Pik s'engage à notifier la CNIL dans les 72 heures et à informer les personnes concernées si la violation est susceptible d'engendrer un risque élevé pour leurs droits et libertés, conformément aux articles 33 et 34 du RGPD.</p>

